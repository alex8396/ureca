import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        
        // 덧셈식을 출력합니다.
        System.out.println(a + " + " + b + " = " + (a + b));
    }
}

import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        
        // 두 문자열을 입력받음
        String str1 = sc.next();
        String str2 = sc.next();
        
        // 두 문자열을 이어서 출력
        System.out.println(str1 + str2);
    }
}
